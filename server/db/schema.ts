// ─── Subscribers ────────────────────────────────────────────────────────────

/**
 * Newsletter subscribers. Captured via the public Subscribe form; the
 * confirmation flow follows the classic double-opt-in pattern (a token
 * is emailed, the user clicks the link to confirm). Subscribers don't
 * need a user account — they're a separate, lighter-weight identity.
 *
 * `source` records WHERE the subscription came from (sidebar, hero,
 * subscribe modal, edition footer) so audience-growth can attribute
 * conversions to specific touchpoints.
 */
export const subscribers = mysqlTable("subscribers", {
  id: int("id").autoincrement().primaryKey(),
  email: varchar("email", { length: 320 }).notNull().unique(),
  name: varchar("name", { length: 128 }),
  /** Token used to confirm the email (double opt-in). Cleared on
   *  confirm. NULL once confirmed. */
  confirmToken: varchar("confirmToken", { length: 64 }),
  confirmedAt: timestamp("confirmedAt"),
  unsubscribedAt: timestamp("unsubscribedAt"),
  /** Free-form source attribution: "sidebar", "modal", "hero",
   *  "edition-footer", etc. */
  source: varchar("source", { length: 64 }),
  /** True when the subscriber has a paid tier on Substack. Synced
   *  manually or by webhook in production. */
  isPremium: boolean("isPremium").default(false).notNull(),
  createdAt: timestamp("createdAt").defaultNow().notNull(),
});

export type Subscriber = typeof subscribers.$inferSelect;
export type InsertSubscriber = typeof subscribers.$inferInsert;

// ─── Daily key metrics ──────────────────────────────────────────────────────

/**
 * Daily-refreshed market metrics shown in the Today page strip. One row
 * per metric (keyed by `metricKey`), upserted by the daily-metrics
 * GitHub Actions workflow. The `previousValue` is preserved across
 * updates so the UI can render a "vs. prior" delta without storing
 * history elsewhere.
 */
export const dailyMetrics = mysqlTable("daily_metrics", {
  id: int("id").autoincrement().primaryKey(),
  /** Stable key — "asx200", "audusd", "cash_rate". Used as the upsert key. */
  metricKey: varchar("metricKey", { length: 64 }).notNull().unique(),
  /** Human label — "ASX 200", "AUD / USD", "Cash rate". */
  label: varchar("label", { length: 128 }).notNull(),
  /** Current value as a display string, e.g. "8,210.43" or "0.6543" or "4.35%". */
  value: varchar("value", { length: 64 }).notNull(),
  /** Optional unit suffix, e.g. "%", "pts". UI may append. */
  unit: varchar("unit", { length: 16 }),
  /** Previous value before this update — used to compute deltas in the UI. */
  previousValue: varchar("previousValue", { length: 64 }),
  /** Source label — "Yahoo Finance", "RBA", "Manual". */
  source: varchar("source", { length: 64 }),
  /** Editorial context blurb under the value, e.g.
   *  "ANZ NOW EXPECTS EXTENDED HOLD" or "BELOW 60% FOR 7 STRAIGHT WEEKS". */
  context: varchar("context", { length: 256 }),
  /** Group key for organising tiles into sections on the metrics
   *  dashboard — "MACRO", "PROPERTY", "LABOUR", "MARKETS", "DEMOGRAPHICS". */
  groupKey: varchar("groupKey", { length: 32 }),
  /** Audit trail for news-extracted metrics — the URL the LLM pulled
   *  the figure from. Null for direct API sources (Yahoo / RBA / ABS)
   *  where the source is the canonical URL on the metric definition. */
  sourceUrl: text("sourceUrl"),
  /** As-of timestamp from the data source (not the upsert time). */
  asOf: timestamp("asOf").notNull(),
  /** Display order, lower = earlier. */
  displayOrder: int("displayOrder").default(100).notNull(),
  updatedAt: timestamp("updatedAt").defaultNow().notNull(),
});

export type DailyMetric = typeof dailyMetrics.$inferSelect;
export type InsertDailyMetric = typeof dailyMetrics.$inferInsert;

/**
 * Append-only history of metric values. One row per upsert, so we can
 * draw a sparkline next to the current value on the dashboard. Cheap to
 * write (rate is one row per metric per day) and bounded — ~30 metrics ×
 * 365 days ≈ 11k rows per year.
 */
export const dailyMetricHistory = mysqlTable(
  "daily_metric_history",
  {
    id: int("id").autoincrement().primaryKey(),
    metricKey: varchar("metricKey", { length: 64 }).notNull(),
    numericValue: double("numericValue").notNull(),
    recordedAt: timestamp("recordedAt").defaultNow().notNull(),
  },
  (t) => ({
    keyRecorded: index("idx_metric_history_key_recorded").on(
      t.metricKey,
      t.recordedAt
    ),
  })
);

export type DailyMetricHistory = typeof dailyMetricHistory.$inferSelect;
export type InsertDailyMetricHistory = typeof dailyMetricHistory.$inferInsert;

// ─── Feedback submissions ──────────────────────────────────────────────────

/**
 * Reader-submitted feedback during the partner-testing window. Captured
 * via the floating Feedback button on every page. Each entry records the
 * reader's note, the page they were on, their user-agent, and an
 * optional contact email so the admin can follow up.
 *
 * `kind` partitions submissions into Bug / Idea / Praise so the inbox
 * is triageable at a glance. `status` flips from "new" to "reviewed"
 * once the admin reads it; nothing's auto-archived.
 */
export const feedbackSubmissions = mysqlTable("feedback_submissions", {
  id: int("id").autoincrement().primaryKey(),
  kind: varchar("kind", { length: 16 }).notNull(), // "bug" | "idea" | "praise"
  message: text("message").notNull(),
  pageUrl: varchar("pageUrl", { length: 512 }),
  userAgent: varchar("userAgent", { length: 512 }),
  contactEmail: varchar("contactEmail", { length: 320 }),
  /** Free-form reporter label — "Ruben's friend Sarah", "broker tester",
   *  etc. Captured so the admin doesn't have to remember who submitted
   *  what during a wide testing window. */
  reporterLabel: varchar("reporterLabel", { length: 128 }),
  status: varchar("status", { length: 16 }).default("new").notNull(),
  createdAt: timestamp("createdAt").defaultNow().notNull(),
});

export type FeedbackSubmission = typeof feedbackSubmissions.$inferSelect;
export type InsertFeedbackSubmission = typeof feedbackSubmissions.$inferInsert;

// ─── Edition asset blobs (hero + substack images) ──────────────────────────

/**
 * Binary storage for AI-generated edition images. Lives in its own table
 * so the large mediumblob never travels with editions.list / getById
 * unless explicitly selected. The `editions.heroImageUrl` and
 * `editions.substackDraftImageUrl` columns store a short
 * `/api/images/edition/:id/:kind` URL pointing at a row in here.
 *
 * One row per (editionId, kind). Re-generating overwrites the latest
 * row rather than appending — historical images aren't useful and
 * blob churn is cheap at this scale.
 */
export const editionAssets = mysqlTable("edition_assets", {
  id: int("id").autoincrement().primaryKey(),
  editionId: int("editionId").notNull(),
  /** "hero" or "substack". Free-form short string keeps future asset
   *  kinds (story thumbs, etc.) easy without another schema change. */
  kind: varchar("kind", { length: 32 }).notNull(),
  contentType: varchar("contentType", { length: 64 }).notNull(),
  /** WebP / PNG bytes. mediumblob = up to 16MB; we compress to <300KB. */
  bytes: mediumBlob("bytes").notNull(),
  createdAt: timestamp("createdAt").defaultNow().notNull(),
});

export type EditionAsset = typeof editionAssets.$inferSelect;
export type InsertEditionAsset = typeof editionAssets.$inferInsert;

// ─── Featured LinkedIn posts ────────────────────────────────────────────────

/**
 * Hand-curated LinkedIn posts that surface on the Today page as a "Ruben
 * on LinkedIn" strip. LinkedIn has no scrape-friendly API, so the admin
 * pastes the URL + excerpt manually. The site renders cards that link
 * back to the original post — readers finish the read on LinkedIn, which
 * doubles as engagement / follower acquisition for the brand.
 */
export const featuredLinkedInPosts = mysqlTable("featured_linkedin_posts", {
  id: int("id").autoincrement().primaryKey(),
  /** Canonical URL to the LinkedIn post (linkedin.com/posts/... or /feed/update/...). */
  postUrl: varchar("postUrl", { length: 1024 }).notNull(),
  /** 1-3 sentence pull from the post body. Plain text, no markup. */
  excerpt: text("excerpt").notNull(),
  /** Author display name; defaults to Ruben. Stored so we can feature
   *  guest posts down the line. */
  authorName: varchar("authorName", { length: 128 }).default("Ruben Laubscher").notNull(),
  /** Lower numbers sort first. Default 100 so new entries land at the end. */
  displayOrder: int("displayOrder").default(100).notNull(),
  /** Soft-hide flag — set false to remove from the page without deleting
   *  the row. */
  isLive: boolean("isLive").default(true).notNull(),
  createdAt: timestamp("createdAt").defaultNow().notNull(),
});

export type FeaturedLinkedInPost = typeof featuredLinkedInPosts.$inferSelect;
export type InsertFeaturedLinkedInPost = typeof featuredLinkedInPosts.$inferInsert;

