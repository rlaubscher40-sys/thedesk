/**
 * Drizzle schema for The Desk. JSON columns are typed against the Zod-derived
 * shapes in shared/schemas.ts so the database, server and client all agree on
 * what is in there.
 */
import {
  boolean,
  customType,
  double,
  index,
  int,
  json,
  mysqlEnum,
  mysqlTable,
  text,
  timestamp,
  varchar,
} from "drizzle-orm/mysql-core";

/**
 * MEDIUMBLOB column type — up to 16MB binary, plenty for compressed
 * hero images. Drizzle ships `binary` and `varbinary` but not blob,
 * so we declare it via customType and let mysql2 bind Buffers natively.
 */
const mediumBlob = customType<{ data: Buffer; driverData: Buffer }>({
  dataType: () => "mediumblob",
});
import type { EditionTopic, KeyMetrics, Signals } from "../../shared/schemas";

// ─── Users ──────────────────────────────────────────────────────────────────

export const users = mysqlTable("users", {
  id: int("id").autoincrement().primaryKey(),
  openId: varchar("openId", { length: 64 }).notNull().unique(),
  name: text("name"),
  email: varchar("email", { length: 320 }),
  loginMethod: varchar("loginMethod", { length: 64 }),
  role: mysqlEnum("role", ["user", "admin"]).default("user").notNull(),
  /** True when the user has an active paid subscription. The PaywallHint
   *  uses this to decide whether to render the soft paywall. */
  isPremium: boolean("isPremium").default(false).notNull(),
  createdAt: timestamp("createdAt").defaultNow().notNull(),
  updatedAt: timestamp("updatedAt").defaultNow().onUpdateNow().notNull(),
  lastSignedIn: timestamp("lastSignedIn").defaultNow().notNull(),
});

export type User = typeof users.$inferSelect;
export type InsertUser = typeof users.$inferInsert;

// ─── Editions ───────────────────────────────────────────────────────────────

export const editions = mysqlTable("editions", {
  id: int("id").autoincrement().primaryKey(),
  editionNumber: int("editionNumber").notNull().unique(),
  weekOf: varchar("weekOf", { length: 64 }).notNull(),
  weekRange: varchar("weekRange", { length: 128 }).notNull(),
  publishedAt: timestamp("publishedAt").defaultNow().notNull(),
  pdfUrl: text("pdfUrl"),
  readingTime: varchar("readingTime", { length: 32 }),
  /** Topic cards rendered in the Edition reader. */
  topics: json("topics").$type<EditionTopic[]>().notNull(),
  /** Short signal briefs. */
  signals: json("signals").$type<Signals>().notNull(),
  /** Full-text content of the source PDF for the search procedure. */
  fullText: text("fullText"),
  /** Week-over-week comparable metrics. */
  keyMetrics: json("keyMetrics").$type<KeyMetrics>(),
  /** AI-generated hero image URL. */
  heroImageUrl: text("heroImageUrl"),
  /** 2-4 sentence editorial opinion in Ruben's voice. */
  rubensTake: text("rubensTake"),
  /** Substack essay draft (saved through the admin panel). */
  substackDraftTitle: text("substackDraftTitle"),
  substackDraftSubtitle: text("substackDraftSubtitle"),
  substackDraftBody: text("substackDraftBody"),
  substackDraftImageUrl: text("substackDraftImageUrl"),
  /** Overall market stress signal — "low" / "moderate" / "high". Drives a
   *  single coloured dot on the edition hero, the way The Signal does. */
  marketStress: varchar("marketStress", { length: 16 }),
  /** Edition-level "dates to watch" forward calendar. JSON array of
   *  `{label, description}` entries (label is something like "May 21"
   *  or "Ongoing"). */
  datesToWatch: json("datesToWatch").$type<DateToWatch[] | null>(),
  /** SEO-optimised page title (~60 chars). Generated by the headline
   *  optimiser pass; falls back to the lead topic title. */
  metaTitle: varchar("metaTitle", { length: 160 }),
  /** SEO meta description (~155 chars) for the edition's page. */
  metaDescription: varchar("metaDescription", { length: 320 }),
  /** Social-share title (~70 chars) — punchier than metaTitle. */
  socialTitle: varchar("socialTitle", { length: 200 }),
  /** Social-share description (~200 chars). */
  socialDescription: varchar("socialDescription", { length: 400 }),
  /** Three alternative web headlines for the edition. The admin can
   *  promote one to the live edition or use them as A/B variants. */
  headlineVariants: json("headlineVariants").$type<string[] | null>(),
  createdAt: timestamp("createdAt").defaultNow().notNull(),
});

export type DateToWatch = {
  label: string;
  description: string;
};

export type Edition = typeof editions.$inferSelect;
export type InsertEdition = typeof editions.$inferInsert;

